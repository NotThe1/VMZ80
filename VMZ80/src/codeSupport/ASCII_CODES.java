package codeSupport;

public final class ASCII_CODES {
	public static final char NUL = 0X00; // CTRL @ Null Prompt
	public static final char SOH = 0X01; // CTRL A Start of Heading
	public static final char STX = 0X02; // CTRL B Start of Text
	public static final char ETX = 0X03; // CTRL C End of Text
	public static final char EOT = 0X04; // CTRL D End of Transmission
	public static final char ENQ = 0X05; // CTRL E Enquiry
	public static final char ACK = 0X06; // CTRL F Acknowledgement
	public static final char BEL = 0X07; // CTRL G Bell

	public static final char BS = 0X08; // CTRL H Backspace
	public static final char TAB = 0X09; // CTRL I Horizontal Tab
	public static final char LF = 0X0A; // CTRL J Line feed
	public static final char VT = 0X0B; // CTRL K Vertical Tab
	public static final char FF = 0X0C; // CTRL L Form Feed
	public static final char CR = 0X0D; // CTRL M Carriage Return
	public static final char SO = 0X0E; // CTRL N Shift out
	public static final char SI = 0X0F; // CTRL O Shift In

	public static final char DLE = 0X10; // CTRL P Data Link Escape
	public static final char XON = 0X11; // CTRL Q X-ON
	public static final char DC2 = 0X12; // CTRL R DC-2
	public static final char XOFF = 0X13; // CTRL S X-OFF
	public static final char DC4 = 0X14; // CTRL T DC-4
	public static final char NAK = 0X15; // CTRL U No Acknowledge
	public static final char SYN = 0X16; // CTRL V Synchronous Idle
	public static final char ETB = 0X17; // CTRL W End transmission Block

	public static final char CAN = 0X18; // CTRL X Cancel
	public static final char EM = 0X19; // CTRL Y End of Medium
	public static final char SUB = 0X1A; // CTRL Z Substitute
	public static final char ESC = 0X1B; // CTRL [ Escape
	public static final char FS = 0X1C; // CTRL \ File Separator
	public static final char GS = 0X1D; // CTRL ] Group Separator
	public static final char RS = 0X1E; // CTRL ^ Record Separator
	public static final char US = 0X1F; // CTRL _ Unit Separator

	public static final char SPACE = 0X20; // space

}// class ASCII_CODES
